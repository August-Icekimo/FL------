# 判斷是否需要經過「人事組長」簽核關卡

# === 強制進入 (Quick Pass) ===

# 若假別為防疫相關假別或工會會務假，必須經過人事組長簽核
# 117: (不支薪)防疫照顧假, 118: 防疫病假, 119: 防疫隔離假, 120: (不支薪)疫苗接種假, 67, 165, 166: 會務假
if vaid in (67, 117, 118, 119, 120, 165, 166):
    return True

# 若部門為第一工廠(1)或第二工廠(2)，且職級在「組長/課長」(700)(含)以下，且假別為「公務」(6)，則須簽核
if plevel in (1, 2) and nlevel<=700 and vaid==6:
  return True

# 針對職級在「副單位主管」(750)(含)以下，且非第一工廠(1)、第二工廠(2)之人員：
# 若假別「非」上述常見免簽假別（公出4/公假(本廠)7/各類補休/休假20/遞延休假21/事假12/病假14），
# 「或者」請假時數 > 8 小時，
# 「或者」連續請假天數 > 1 天，則須經過人事組長簽核
if nlevel<=750 and plevel not in (1, 2) and (vaid not in (4, 7, 12, 14, 20, 21, 35, 36, 37, 38, 109, 110, 111) or hours>8 or continueDays>1):
    return True

# === 排除條件 ===

# 若假別為公假(本廠公務)(7)、公差(5)、遞延休假(21)，則跳過此關
# （註：已移除原本冗餘且不會被執行的理監事會務假判斷）
if vaid in (5, 7, 21):
    return False

# 針對第一工廠(1)或第二工廠(2)，除上述條件外，預設不經過人事組長
if plevel in (1, 2):
    return False

# 職級在「單位主管」(800)(含)以上者，不經過人事組長
if nlevel>=800: #單位主管以上
   return False

# 針對職級為「副單位主管」(750) 的請假條件：
# 1. 事假(12)、休假(20)、遞延休假(21)、特准病假(15) 且 請假時數 <= 8 小時
# 2. 公出(4)、公假(本廠公務)(7)、及各類補休 (35, 36, 37, 38, 109, 110, 111)
# 若符合上述任一條件，跳過人事組長簽核
if nlevel==750 and ((vaid in (12, 15, 20, 21) and hours<=8) or vaid in (4, 7, 35, 36, 37, 38, 109, 110, 111)):
   return False

# 針對職級在「副單位主管」(750)(含)以下，且非第一工廠(1)、第二工廠(2)之人員：
# 若為公出(4)、公假(本廠公務)(7) 或 各類補休 (35, 36, 37, 38, 109, 110, 111)，跳過人事組長簽核
if nlevel<=750 and plevel not in (1, 2) and vaid in (4, 7, 35, 36, 37, 38, 109, 110, 111):
    return False

# 預設跳過此關卡
return False
